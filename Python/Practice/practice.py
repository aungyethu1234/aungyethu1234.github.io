list = [12, 13, 14]
print(list)
print(list[:2])
print(list[0:-1])

tuple = (1, "two")
#looping for list
for item in list :
    print(item)

print(tuple)
#tuple looping 
for data in tuple:
    print(data)

for d in range(1,6,2):
    print(d)

dict = {'name': 'Mg Mg', 'age': 24}
print(dict)
#looping for dictionary
for key in dict:
    print(key(dict.keys))
